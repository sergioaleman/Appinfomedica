	<p><b><span style="font-size: 120%">Â¿Qu&eacute; son el sobrepeso y la obesidad?</span></b></p>
	<p>El sobrepeso y la obesidad se definen como una acumulaci&oacute;n anormal o excesiva de grasa que puede ser perjudicial para la salud.<br><br>
	El <b>&iacute;ndice de masa corporal (IMC)</b> es un indicador simple de la 
relaci&oacute;n entre el peso y la talla que se utiliza frecuentemente
 	para identificar el sobrepeso y la obesidad en los adultos. Se calcula
 dividiendo el peso de una persona en kilogramos por el cuadrado de su 
talla en metros (kg/m2).<br><br>
 	Un IMC elevado es un importante factor de riesgo de enfermedades no transmisibles, como:
 
 	</p><ul>
 		<li>Las enfermedades cardiovasculares (principalmente cardiopat&iacute;a y accidente cerebrovascular)
 		</li><li>La diabetes
 		</li><li>Los trastornos del aparato locomotor (en especial la 
osteoartritis, una enfermedad degenerativa de las articulaciones muy 
discapacitante)
 		</li><li>Algunos c&aacute;nceres (del endometrio, la mama y el colon)
 	</li></ul>
 	El riesgo de contraer estas enfermedades no transmisibles crece con el aumento del IMC.
