	<p><b><span style="font-size: 120%">C&oacute;mo puede controlar su peso sin pasar hambre</span></b></p>
	<p>¿Ha tratado de bajar de peso reduciendo la cantidad de alimentos que
 come? ¿Pero despu&eacute;s de comer todav&iacute;a se siente insatisfecho y con 
hambre? ¿O ha evitado tratar de bajar de peso porque tiene miedo de 
sentir hambre todo el tiempo? De ser as&iacute;, usted no es la &uacute;nica que pasa 
por esto; muchas personas literalmente tiran la toalla al tratar de 
bajar peso porque si comen menos se sienten hambrientos o que les falta 
alimento. Pero hay otra manera de hacerlo. Intente bajar de peso de 
manera lenta y constante: disminuya el consumo de calor&iacute;as, mantenga una
 ingesta de nutrientes adecuada y aumente la actividad f&iacute;sica. Usted 
puede reducir las calor&iacute;as sin dejar de comer alimentos nutritivos. La 
clave es consumir alimentos que le llenan sin tener que ingerir una gran
 cantidad de calor&iacute;as.
	<br><br>Las investigaciones demuestran que las personas se llenan por 
la cantidad de alimentos que consumen, y no debido al n&uacute;mero de calor&iacute;as
 que ingieren. Usted puede rebajar las calor&iacute;as de sus comidas favoritas
 recortando la cantidad de grasas o aumentando el contenido de 
ingredientes ricos en fibra, como frutas o verduras.</p>
