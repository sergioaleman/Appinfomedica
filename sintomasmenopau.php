producen durante el climaterio debido a la p&eacute;rdida intermitente del 
control 
	vasomotor perif&eacute;rico, manifest&aacute;ndose en forma de oleadas de calor que 
recorren el cuerpo y suben hasta la cabeza, asociadas a enrojecimiento 
	de la piel del cuello y de la cara, seguido de intensa sudoraci&oacute;n y en 
ocasiones de fuertes palpitaciones y sensaci&oacute;n de angustia. 
	Es lo que se ha dado en llamar s&iacute;ndrome climat&eacute;rico. A esta 
sintomatolog&iacute;a deben agregarse dos factores importantes, uno 
