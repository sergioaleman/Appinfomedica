	<p>Se puede considerar a <b>la menopausia y al climaterio</b> como un 
fen&oacute;meno natural, por cuanto se presenta en todas las mujeres, sin 
excepci&oacute;n a 
	partir de mediados de la d&eacute;cada de los 40 a&ntilde;os. Lo que si no puede 
considerarse natural es el sufrimiento que se genera 
	con el climaterio y las enfermedades que hacen su aparici&oacute;n o se 
incrementan con la posmenopausia. Los cambios hormonales 
	y funcionales que se inician en el climaterio son procesos biol&oacute;gicos, 
pero la reacci&oacute;n neurovegetativa a la involuci&oacute;n ov&aacute;rica 
	en algunas oportunidades es tan intensa que se convierten en s&iacute;ntomas 
