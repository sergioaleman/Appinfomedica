	<p><b><span style="font-size: 120%">Â¿Qu&eacute; es la Diabetes?</span></b></p>
	<p>La diabetes es una afecci&oacute;n cr&oacute;nica que se desencadena cuando el 
organismo pierde su capacidad de producir suficiente insulina o de 
utilizarla con eficacia. La insulina es una hormona que se fabrica en el
 p&aacute;ncreas y que permite que la glucosa de los alimentos pase a las 
c&eacute;lulas del organismo, en donde se convierte en energ&iacute;a para que 
funcionen los m&uacute;sculos y los tejidos. Como resultado, 
	las c&eacute;lulas de una persona con diabetes no absorben la glucosa 
adecuadamente, de modo que &eacute;sta queda circulando en la sangre 
(hiperglicemia) y da&ntilde;ando los tejidos con el paso del tiempo. 
	Hay tres tipos principales de diabetes:</p>
	<ul>
	<li><b>Diabetes Tipo 1:</b> Est&aacute; causada por una reacci&oacute;n autoinmune, 
en la que el sistema de defensas del organismo ataca las c&eacute;lulas 
productoras de insulina del p&aacute;ncreas. Como resultado, el organismo deja 
de producir la insulina que necesita.</li>
	<li><b>Diabetes Tipo 2:</b> Es el tipo m&aacute;s com&uacute;n de diabetes. En la 
diabetes tipo 2, el organismo puede producir insulina pero, o bien no es
 suficiente, o el organismo no responde a sus efectos, provocando una 
acumulaci&oacute;n de glucosa en la sangre.</li>
	<li><b>Diabetes mellitus gestacional (DMG):</b> Se dice que una mujer 
tiene diabetes mellitus gestacional (DMG) cuando se le diagnostica 
diabetes por primera vez durante el embarazo.</li>
